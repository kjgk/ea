package com.withub.common.enumeration;

/**
 * 定义数字单位.
 */
public enum NumberUnit {

    /**
     * 个
     */
    Digit,

    /**
     * 十
     */
    Ten,

    /**
     * 百
     */
    Hundred,

    /**
     * 千
     */
    Thousand,

    /**
     * 万
     */
    TenThousand,

    /**
     * 百万
     */
    Million,

    /**
     * 千万
     */
    TenMillion,

    /**
     * 亿
     */
    HundredMillion,
}
